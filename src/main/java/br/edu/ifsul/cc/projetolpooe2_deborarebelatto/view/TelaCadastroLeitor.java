    private void adicionarLeitor() {
        String nome = txtNome.getText();
        String email = txtEmail.getText();

        Leitor leitor = new Leitor();
        leitor.setNome(nome);
        leitor.setEmail(email);

        jpa.persist(leitor);
        visualizarLeitores();
        JOptionPane.showMessageDialog(this, "Leitor adicionado com sucesso!");
    }

    private void editarLeitor() throws Exception {
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            Long id = (Long) table.getValueAt(selectedRow, 0);
            Leitor leitor = (Leitor) jpa.find(Leitor.class, id);
            leitor.setNome(txtNome.getText());
            leitor.setEmail(txtEmail.getText());

            jpa.persist(leitor);
            visualizarLeitores();
            JOptionPane.showMessageDialog(this, "Leitor atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um leitor para editar.");
        }
    }

    private void excluirLeitor() throws Exception {
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            Long id = (Long) table.getValueAt(selectedRow, 0);
            Leitor leitor = (Leitor) jpa.find(Leitor.class, id);
            jpa.remover(leitor);
            visualizarLeitores();
            JOptionPane.showMessageDialog(this, "Leitor exclu√≠do com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um leitor para excluir.");
        }
    }

    private void visualizarLeitores() {
        List<Leitor> leitores = jpa.listLeitor();
        String[] colunas = {"ID", "Nome", "Email"};
        Object[][] dados = new Object[leitores.size()][3];

        for (int i = 0; i < leitores.size(); i++) {
            dados[i][0] = leitores.get(i).getId();
            dados[i][1] = leitores.get(i).getNome();
            dados[i][2] = leitores.get(i).getEmail();
        }

        table = new JTable(dados, colunas);
        JScrollPane scrollPane = new JScrollPane(table);
        add(scrollPane, BorderLayout.CENTER);
        validate();
    }

